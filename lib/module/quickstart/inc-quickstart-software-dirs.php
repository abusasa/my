			<h2>PS3 Game Directory</h2>
		</div>
		<div class="container-tx2-block darkmode-txt">
			<p>
				 Some PlayStation 3 games come in the form of package files, specifically PlayStation Network games, game updates or applications in general. .PKG files must be extracted using RPCS3's package installer found under <span class="highlight darkmode-highlight">File &gt; Install Packages/Raps/Edats</span> (or drag and dropped onto emulator window)
			</p>
			<br>
			<p>
				 Blu-ray disc game data should be placed in <span class="highlight darkmode-highlight">\games</span> located in your RPCS3 root folder or anywhere else except for <span class="highlight darkmode-highlight">\dev_hdd0\game\</span> and can be booted from <span class="highlight darkmode-highlight">File &gt; Boot Game</span> if not present on the game list.
			</p>
			<br>
			<p>
				<b>Note: PlayStation Network games must be placed in <span class="highlight darkmode-highlight">\dev_hdd0\game\</span></b>
			</p>
			<p>
				<b>Note: PlayStation Network .rap files must be placed in <span class="highlight darkmode-highlight">\dev_hdd0\home\00000001\exdata\</span> (or drag and dropped onto emulator window)</b>
			</p>
		</div>
	</div>
</div>
